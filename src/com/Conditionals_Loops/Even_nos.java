package com.Conditionals_Loops;

public class Even_nos {
    public static void main(String[] args) {
        System.out.println("Printing Even nos from 1 to 100");

        for (int i = 0; i <=100 ; i++) {
            if( i % 2 == 0 ){
                System.out.println(i);
            }

        }
    }
}
